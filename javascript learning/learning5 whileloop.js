// looping in javascript;
// looping +1, starting at -100 and ending at 10
// setting the variable at -100
let friendsAtYourParty = -100

// this is a while loop, while friendsAtYourParty is <10 then add +1 and log the current count
while (friendsAtYourParty<10) {
    // adding +1 to the inital count, looping until <10
    friendsAtYourParty = friendsAtYourParty + 1
    // logging current count
    console.log(friendsAtYourParty)
}
console.log("total " + friendsAtYourParty)