// ifstatements in java script, also known as FLOW CONTROL



// setting skyIsBlue to true allows the ifstatement below to log the sky is blue
const skyIsBlue = true;
// using ifstatements, we log the sky is blue
if (skyIsBlue) {
    // if condition is true, log the sky is blue
    console.log("the sky is blue")
} else {
    // if condition is false, log meow meow
    console.log("meow meow")
}